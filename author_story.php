        <p><b>Name:</b> Cassandra Morgan</p>
<p><b>Autorenname:</b> Cass Morgan</p>
<p><b>Geburtsdatum:</b> 30. August 1951</p>

<p><b>Geburtsort:</b> Louisville, Kentucky, USA</p>

<p><b>Ausbildung:</b> Cass Morgan studierte an der Universität von Kentucky in Lexington und erwarb einen Bachelor-Abschluss in Schauspielkunst. Später absolvierte sie auch ein Masterstudium in Darstellender Kunst an der Yale School of Drama.</p>

<p><b>Karriere:</b> Cass Morgan ist nicht nur eine erfolgreiche Schauspielerin und Sängerin, sondern auch eine talentierte Autorin. Sie hat sowohl Drehbücher als auch Theaterstücke geschrieben.</p>

<p>Ihr erstes Drehbuch war "The Gail O'Grady Project", das 1999 veröffentlicht wurde. Sie schrieb auch das Drehbuch für den Film "Still Holding On: The Legend of Cadillac Jack" aus dem Jahr 1998.</p>

<p>Als Theaterautorin hat Morgan mehrere erfolgreiche Stücke verfasst. Ihr Stück "The Road to Where" wurde 1991 in New York uraufgeführt. Sie schrieb auch das Buch für das Musical "Pump Boys and Dinettes", das 1982 am Off-Broadway Premiere hatte und später auch am Broadway lief.</p>

<p>Morgan ist auch bekannt für ihr Stück "True Home", das 2001 in New York aufgeführt wurde und für das sie den Kleban Prize for Most Promising Musical Theater Librettist erhielt. Sie schrieb auch das Buch für das Musical "Ring of Fire", das auf den Songs von Johnny Cash basiert und 2006 am Broadway Premiere hatte.</p>

<p><b>Persönliches Leben:</b> Cass Morgan ist verheiratet und hat zwei Kinder. Sie setzt sich auch für soziale und Umweltfragen ein und engagiert sich für verschiedene gemeinnützige Organisationen.</p>

<p><b>Auszeichnungen:</b> Cass Morgan wurde für ihre Arbeit als Autorin mehrfach ausgezeichnet, darunter mit dem Kleban Prize for Most Promising Musical Theater Librettist für ihr Stück "True Home".</p></div>
